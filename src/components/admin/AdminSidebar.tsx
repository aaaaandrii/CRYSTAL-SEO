'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { signOut, useSession } from 'next-auth/react';

const navItems = [
  { href: '/admin', label: 'Dashboard', icon: DashboardIcon },
  { href: '/admin/pages', label: 'Pages', icon: PagesIcon },
  { href: '/admin/news', label: 'News', icon: NewsIcon },
  { href: '/admin/case-studies', label: 'Case Studies', icon: CaseStudyIcon },
  { href: '/admin/gallery', label: 'Gallery', icon: GalleryIcon },
];

const adminOnlyItems = [
  { href: '/admin/users', label: 'Users', icon: UsersIcon },
];

export function AdminSidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();
  const isAdmin = session?.user?.role === 'admin';
  const allNavItems = isAdmin ? [...navItems, ...adminOnlyItems] : navItems;

  return (
    <aside className="fixed left-0 top-0 h-screen w-64 bg-brand-navy border-r border-brand-border flex flex-col">
      <div className="p-6 border-b border-brand-border">
        <Link href="/admin" className="text-brand-white font-bold text-lg">
          5D Crystal Admin
        </Link>
      </div>

      <nav className="flex-1 py-4">
        {allNavItems.map((item) => {
          const isActive =
            item.href === '/admin'
              ? pathname === '/admin'
              : pathname.startsWith(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-6 py-3 text-sm transition-colors ${
                isActive
                  ? 'bg-brand-accent/10 text-brand-accent border-r-2 border-brand-accent'
                  : 'text-brand-gray hover:text-brand-white hover:bg-brand-dark/50'
              }`}
            >
              <item.icon className="w-5 h-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="p-4 border-t border-brand-border">
        <Link
          href="/"
          className="flex items-center gap-2 px-4 py-2 text-sm text-brand-gray hover:text-brand-white transition-colors mb-2"
        >
          <ExternalLinkIcon className="w-4 h-4" />
          View Site
        </Link>
        <button
          onClick={() => signOut({ callbackUrl: '/admin/login' })}
          className="flex items-center gap-2 px-4 py-2 text-sm text-brand-gray hover:text-red-400 transition-colors w-full text-left"
        >
          <LogOutIcon className="w-4 h-4" />
          Sign Out
        </button>
      </div>
    </aside>
  );
}

